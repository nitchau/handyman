"use client";

import { useState } from "react";
import { AlertTriangle, Info } from "lucide-react";
import { useTranslation } from "@/lib/i18n/language-context";

interface DisclaimerBannerProps {
  referenceDetected?: boolean;
  confidenceScore?: number;
}

export function DisclaimerBanner({
  referenceDetected,
  confidenceScore,
}: DisclaimerBannerProps) {
  const { t } = useTranslation();
  const [accepted, setAccepted] = useState(false);

  if (accepted) return null;

  const showRefTip =
    confidenceScore !== undefined &&
    confidenceScore < 0.5 &&
    !referenceDetected;

  return (
    <div className="space-y-3">
      <div className="rounded-lg border border-amber-300 bg-amber-50 p-4">
        <div className="flex gap-3">
          <AlertTriangle className="mt-0.5 size-5 shrink-0 text-amber-500" />
          <div className="flex-1 space-y-2">
            <p className="text-sm font-medium text-amber-800">
              Handy-Generated Estimate
            </p>
            <p className="text-sm text-amber-700">
              This project plan and cost estimate were generated by Handy based on
              your photos and description. Actual materials, quantities, and
              prices may vary. We recommend consulting a professional before
              making purchases.
            </p>
            <label className="flex items-center gap-2">
              <input
                type="checkbox"
                checked={accepted}
                onChange={(e) => setAccepted(e.target.checked)}
                className="size-4 rounded border-amber-400 text-amber-600 focus:ring-amber-500"
              />
              <span className="text-sm font-medium text-amber-800">
                I understand this is an estimate
              </span>
            </label>
          </div>
        </div>
      </div>

      {showRefTip && (
        <div className="rounded-lg border border-amber-200 bg-amber-50 p-3">
          <div className="flex gap-2">
            <Info className="mt-0.5 size-4 shrink-0 text-amber-500" />
            <p className="text-sm text-amber-700">
              {t("plan.results.lowConfidenceWarning")}
            </p>
          </div>
        </div>
      )}
    </div>
  );
}
